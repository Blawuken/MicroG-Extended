package com.google.android.gms.mobstore;

import android.net.Uri;
import android.os.Parcel;

import androidx.annotation.NonNull;

import com.google.android.gms.common.internal.safeparcel.AbstractSafeParcelable;
import com.google.android.gms.common.internal.safeparcel.SafeParcelable;
import com.google.android.gms.common.internal.safeparcel.SafeParcelableCreatorAndWriter;

@SafeParcelable.Class
public class DeleteFileRequest extends AbstractSafeParcelable {

    @Field(value = 1, getterName = "getFileUri")
    private Uri fileUri;

    public Uri getFileUri() {
        return fileUri;
    }

    @Override
    public void writeToParcel(@NonNull Parcel dest, int flags) {
        CREATOR.writeToParcel(this, dest, flags);
    }

    public static final SafeParcelableCreatorAndWriter<DeleteFileRequest> CREATOR = findCreator(DeleteFileRequest.class);
}
